export default function sum(a, b) {
  return a + b;
}

export function substract(a, b) {
  return a - b;
}

export function multiply(a, b) {
  return a * b;
}

export function divide(a, b) {
  return a / b;
}

console.log(sum(4, 5));
console.log(substract(8, 4));
console.log(multiply(6, 4));
console.log(divide(8, 2));
